public class Day050 {
    static int penjumlahan(int x, int y){
        return x + y;
        
    }
    static double perkalian(double a, double b){
        return a * b;
    }
    public static void main(String[] args) {
        int mypenjumlahan = penjumlahan(3, 2);
        double myperkalian = perkalian(10.0, 5.5);
        System.out.println(mypenjumlahan);
        System.out.println(myperkalian);
        
    }

    
}