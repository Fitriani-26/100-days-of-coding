public class Day041 {
    public static void main(String[] args) {
        int angka1 = 5;
        int angka2 = 4;
        //Decrement
        System.out.println(--angka2);
        System.out.println(angka2--);
        System.out.println("Nilai Akhir " + angka2);
        //Increment
        System.out.println(++angka1);
        System.out.println(angka1++);
        System.out.println("Nilai Akhir " + angka1);
    }
    
}
