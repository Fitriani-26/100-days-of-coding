public class Day071 {
    public static void main(String[] args) {
        StringBuffer sc = new StringBuffer("Selamat Menikmati");

        // Menampilkan isi awal
        System.out.println("Isi Awal: " + sc);

        // Membalik urutan karakter menggunakan reverse
        sc.reverse();

        // Menampilkan hasil setelah pembalikan
        System.out.println("Setelah Pembalikan: " + sc);
    }
    
}
